def format_enabled_groups(groups: list[RuntimeGroup]) -> str:
    if not groups:
        return "??????????"
    lines = ["??????"]
    for group in groups:
        lines.append(
            f"{group.title or '????'} | chat_id={group.chat_id} | ????={format_dt(group.enabled_at)}"
        )
    return "\n".join(lines)


